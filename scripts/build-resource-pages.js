import { mkdir, readdir, readFile, unlink, writeFile } from "node:fs/promises";
import { join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

import { resources } from "../resource.js";

export const GENERATED_MARKER = "<!-- Generated by scripts/build-resource-pages.js; do not edit. -->";

const scriptPath = fileURLToPath(import.meta.url);
const defaultOutputDirectory = fileURLToPath(new URL("../resources/", import.meta.url));

function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

function renderSteps(steps) {
  return steps.map((step, index) => `
              <li><span>${String(index + 1).padStart(2, "0")}</span><p>${escapeHtml(step)}</p></li>`).join("");
}

function renderLinks(links) {
  return links.map((link) => `
              <a class="detail-link" href="${escapeHtml(link.url)}">
                <span>${escapeHtml(link.label)}</span>
                <strong aria-hidden="true">↗</strong>${link.note ? `
                <small>${escapeHtml(link.note)}</small>` : ""}
              </a>`).join("");
}

function renderNote(note) {
  if (!note) return "";

  return `

      <aside class="resource-note">
        <p class="panel-label">Important context</p>
        <p>${escapeHtml(note)}</p>
      </aside>`;
}

