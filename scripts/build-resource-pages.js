async function removeStaleGeneratedPages(outputDirectory, expectedFiles) {
  const entries = await readdir(outputDirectory, { withFileTypes: true });

  await Promise.all(entries.map(async (entry) => {
    if (!entry.isFile() || !entry.name.endsWith(".html") || expectedFiles.has(entry.name)) return;

    const path = join(outputDirectory, entry.name);
    const contents = await readFile(path, "utf8");
    if (contents.includes(GENERATED_MARKER)) await unlink(path);
  }));
}

export async function buildResourcePages({
  resourceList = resources,
  outputDirectory = defaultOutputDirectory,
} = {}) {
  await mkdir(outputDirectory, { recursive: true });

  const expectedFiles = new Set(resourceList.map(({ slug }) => `${slug}.html`));
  await removeStaleGeneratedPages(outputDirectory, expectedFiles);

  return Promise.all(resourceList.map(async (resource) => {
    const outputPath = join(outputDirectory, `${resource.slug}.html`);
    await writeFile(outputPath, renderResourcePage(resource), "utf8");
    return outputPath;
  }));
}

const invokedPath = process.argv[1] ? resolve(process.argv[1]) : "";
if (invokedPath === resolve(scriptPath)) {
  await buildResourcePages();
}
